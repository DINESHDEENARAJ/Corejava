package dhennabasic.controlstatementsif;

        public class elseif
        {
            public static void main(String[] args)// one condition multiple statement.
            {
                String name = "dhayalan";  // string name same as else-if.

                if (name == "deena")
                {
                    System.out.println("name is deena");
                }
                else if (name == "dhayalan")
                {
                    System.out.println("name is dhayalan");
                }
                else
                {
                    System.out.println("no answer");
                }
            }
        }
