package StringMethods;

public class concordinate
{
    public static void main(String[] args)
    {
        String d="jai "+ "gokul " + "dee ";
        System.out.println(d);

        String d1="car";
        String d2="van";
        String d3="bus";
        System.out.println(d1 . concat (d2));
        System.out.println(d2 . concat (d3));
        System.out.println(d3 . concat (d1));
    }
}
